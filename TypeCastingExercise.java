class TypeCastingExercise {
    public static void main(String[] args) {
        
        double d = 100.98;
        int i = (int) d;
         System.out.println("Double convertido a int: " + i);
         
         int a = 80;
         double b = (double) a;
         System.out.println("Int convertido a double: " + b);
    }
}